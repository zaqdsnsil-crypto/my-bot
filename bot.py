# ==================== توابع ====================

def get_main_keyboard():
    keyboard = [
        [KeyboardButton("🎡 شهر بازی"), KeyboardButton("💕 خاطرات عشق")],
        [KeyboardButton("😢 روزهای تنها"), KeyboardButton("🤖 مدیریت گروه")],
        [KeyboardButton("📖 رمان"), KeyboardButton("🎉 سرگرمی")],
        [KeyboardButton("🎵 موزیک دلخواه")]
    ]
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)

def get_game_keyboard():
    keyboard = [
        [KeyboardButton("🎭 کوییز مافیا"), KeyboardButton("💪 برنامه تمرینی")],
        [KeyboardButton("🏠 برگشت به منو")]
    ]
    return ReplyKeyboardMarkup(keyboard, resize_keyboard=True)

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "سلام! 👋\nبه بات خوش اومدی!\nیه گزینه رو انتخاب کن 👇",
        reply_markup=get_main_keyboard()
    )

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    text = update.message.text

    if text == "💕 خاطرات عشق":
        await update.message.reply_text(random.choice(پیام_عاشقانه))

    elif text == "😢 روزهای تنها":
        await update.message.reply_text(random.choice(نوشته_غمگین))
        await update.message.reply_text("🌧️ تنها نیستی، منم اینجام...")

    elif text == "🎉 سرگرمی":
        await update.message.reply_text(random.choice(جوک))

    elif text == "📖 رمان":
        await update.message.reply_text(random.choice(داستان))

    elif text == "🤖 مدیریت گروه":
        await update.message.reply_text(random.choice(آموزش_مدیریت))

    elif text == "🎡 شهر بازی":
        await update.message.reply_text("🎡 شهر بازی\nیه بخش رو انتخاب کن:", reply_markup=get_game_keyboard())

    elif text == "🎭 کوییز مافیا":
        سوال = random.choice(سوال_مافیا)
        context.user_data["جواب_مافیا"] = سوال["جواب"]
        context.user_data["توضیح_مافیا"] = سوال["توضیح"]
        context.user_data["در_کوییز"] = True
        await update.message.reply_text(سوال["سوال"] + "\n\n✏️ جوابت رو بنویس (الف، ب، ج یا د)")

    elif text == "💪 برنامه تمرینی":
        await update.message.reply_text(random.choice(برنامه_تمرینی))

    elif text == "🎵 موزیک دلخواه":
        context.user_data["جستجوی_موزیک"] = True
        await update.message.reply_text("🎵 اسم آهنگ یا خواننده رو بنویس 👇\n\nمثال: ماکان بند یا شادمهر")

    elif text == "🏠 برگشت به منو":
        await update.message.reply_text("منوی اصلی 👇", reply_markup=get_main_keyboard())

    elif context.user_data.get("در_کوییز"):
        جواب_کاربر = text.strip()
        جواب_درست = context.user_data.get("جواب_مافیا", "")
        توضیح = context.user_data.get("توضیح_مافیا", "")
        if جواب_کاربر == جواب_درست:
            await update.message.reply_text(f"✅ آفرین! جواب درسته! 🎉\n\n{توضیح}")
        else:
            await update.message.reply_text(f"❌ اشتباه! جواب درست {جواب_درست} بود!\n\n{توضیح}")
        context.user_data["در_کوییز"] = False

    elif context.user_data.get("جستجوی_موزیک"):
        await update.message.reply_text(
            f"🔍 دنبال {text} می‌گردم...\n\n🎵 برای دانلود موزیک به این سایت‌ها برو:\n\n"
            f"🔗 radiojavan.com\n"
            f"🔗 bia2music.com\n"
            f"🔗 navahang.com\n\n"
            f"اسم آهنگ: {text} رو اونجا سرچ کن! 🎶"
        )
        context.user_data["جستجوی_موزیک"] = False

    else:
        await update.message.reply_text("لطفاً از دکمه‌های پایین استفاده کن 👇", reply_markup=get_main_keyboard())

# ==================== اجرا ====================

def main():
    app = Application.builder().token(TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    print("✅ بات شروع به کار کرد!")
    app.run_polling()

if __name__ == "__main__":
    main()
